#include<stdio.h>
int main()
{
int a=5;
printf("%d \n",a++); //print then increment(so out put 5)then a=6
printf("%d \n",++a); //increment then print a=6+1=7 (so output 7)
printf("%d \n",a--); //a=7 printed thereafter decrement so a=6
printf("%d \n",--a); //a=6-1 = 5 then print the value of a i.e, 5
}
